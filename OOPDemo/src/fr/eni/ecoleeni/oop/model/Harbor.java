package fr.eni.ecoleeni.oop.model;

public class Harbor {
	
	private String name;
	private Boat boat;
	
	public Harbor() {
		
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Boat getBoat() {
		return boat;
	}
	public void setBoat(Boat boat) {
		this.boat = boat;
	}
	
	

}
